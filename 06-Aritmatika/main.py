#operasi aritmatika

a = 5
b = 2
#penjumlahan (+)
hasil = a + b
print(a,"+",b, "=" ,hasil)

#pengurangan (-)
hasil = a - b
print(a,"-",b, "=" ,hasil)

#perkalian (*)
hasil = a * b
print(a,"*",b, "=" ,hasil)

#pembagian (:)
hasil = a / b
print(a,":",b, "=" ,hasil)

#eksponen (**)
hasil = a ** b
print(a,"**",b, "=" ,hasil)

#modulus (%) sisa pembagian
hasil = a % b
print(a,"%",b, "=" ,hasil)

#floor devision (//)) kebalikan modulus
hasil = a // b
print(a,"//",b, "=" ,hasil)



